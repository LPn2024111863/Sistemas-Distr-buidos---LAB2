from typing import Union

class Dividir:
    def __init__(self, x:float, y:float):
        self.x = x
        self.y = y
        self.res = 0


    def executar(self)->Union[float,str]:
        try:
            self.res = self.x / self.y
        except ZeroDivisionError:
            return "ERRO: Não é possível dividir por 0"
        return self.res